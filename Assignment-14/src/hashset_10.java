import java.util.HashSet;

public class hashset_10 {
    public static void main(String[] args) {
        HashSet<String> hs = new HashSet<String>();

        // Adding elements to the hash set
        hs.add("Apple");
        hs.add("Orange");
        hs.add("Mango");

        // Getting the number of elements in the hash set
        // Printing the number of elements in the hash set
        System.out.println("The number of elements in the hash set is " + hs.size());
    }
}
